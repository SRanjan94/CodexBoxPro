package string;

import java.util.Locale;

public class StringD6 {
    public static void main(String [] args){

        String s="SmrutiRanjan";
        System.out.println("print the position:"+s.charAt(5)); //position of char .charAt()

        System.out.println("for lower case:"+s.toLowerCase());  //for lower case  .toLowerCase()

        System.out.println("for upper case-:"+s.toUpperCase());   //for upper case .toUpperCase()

        System.out.println("for join -:"+s.concat(" Sethi"));   //for adding String  .concat()

        System.out.println("join using + operator-:" +s+" S");   // for adding by + operator

        System.out.println("for length of s-:"+s.length());   //length of string   .length()


    }
}
